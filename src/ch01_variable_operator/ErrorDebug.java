package ch01_variable_operator;

public class ErrorDebug {
    public static void main(String[] args) {
        double a, b, c;
        a = 20;
        b = 7;
        c = a + b;
        System.out.println("a + b =" + c);
        c = a - b;
        System.out.println("a - b =" + c);
        c = a * b;
        System.out.println("a * b = " + c);
        c = a / b;
        System.out.println("a / b =" + c);
    }
}

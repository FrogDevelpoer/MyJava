package ch01_variable_operator;

public class Arithmetric01 {
    public static void main(String[] args) {
        int x = 14;
        int y = 5;

        System.out.println(x + " + " + y + " = " + (x + y));
        System.out.println(x + " - " + y + " = " + (x - y));
        System.out.println(x + " * " + y + " = " + (x * y));
        System.out.println(x + " / " + y + " = " + (x / y));
        System.out.println(x + " % " + y + " = " + (x % y));

        System.out.println(14/5.0);
    }
}
